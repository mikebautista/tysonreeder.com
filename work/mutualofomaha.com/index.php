    <h1 class="page-title">Mutual of Omaha.com 2017 Redesign</h1>

    <div class="content-container">
      <p>On May 15th 2017 Mutual of Omaha was set to launch a brand new marketing campaign. Complete with a television commercial, billboards, digital ads and radio ads the campaign was set to be pretty large. Along with the campaign there was to be a new corporate website with an improved IA and a new look and feel on top of existing content. In July of 2016 I was appointed as lead Visual Designer on the project and in August 2016 we kicked off the project.</p>
      <p>From a visual and interaction standpoint I had a few things I wanted to make sure were addressed in the redesign. So I worked with the team to set some design principles.</p>
      <p>We wanted to reduce the color palette and use color only where necessary. With a focus on high contrast to ensure readability since we were targeting an older demographic.</p>
      <p><ol><li>We wanted clear calls to action and better button hierarchy.</li>
      <li>We wanted to design in a way that would meet WCAG 2.0 Level AA ADA requirements.</li>
      <li>We wanted to freshen the brand and stand out among competitors.</li></ol></p>
      <p>When we began the project Mutualofomaha.com looked like the image below. We were suffering from an identity crisis. We had our work cut out for us.</p>
    </div>

    
    <div class="image-container">
      <img src="/images/old-moo-com.jpeg" />
      <p>Screenshots of different pages on Mutualofomaha.com before the May 15th redesign.</p>
    </div>


    <div class="content-container">
      <p>We started by conducting stakeholder interviews and due to timing restrictions it was decided by leadership that we would not have time to interview customers. Even though this was not how my team would of liked things to work out, we pivoted and created proto-personas to help inform decisions.</p>
      <p>After we had an idea of who we were building the site for and the stakeholder vision we started working on the information architecture. For this we leaned heavily on an IA specialist at the agency. We met with her and worked through the IA which we used to inform the wireframes.</p>
      <p>We tested different versions of wireframes and navigation with customers.</p>
    </div>


