    <div class="content-container">
      <p>If you go to mutualofomaha.com today I bet you can tell which one won. It was the first one.</p>
      <p>From there my team focused on creating internal product pages. We wanted to create templates for the pages so that we could maintain consistency across the site. When trying to do this with the existing content we realized that it was going to clutter the site and that content needed to be discarded and some new content would need to be written.</p>
      <p>Due to the short deadline we had to move through product sections fast to get them to states for approval.</p>
      <p>We knew traditional ways of moving work through the company for approval would not cut it. If we were going to move through sections fast we needed to get everyone in a room and handle concerns right then and there.</p>
      <p>So we started the new workflow. At the beginning of the week we would provide writing templates to the writers for 4 products. We would then take the content they wrote and apply it in the template around the middle of the week. Then towards the end of the week we would have a “pizza party” where we would invite stakeholders, product and compliance into a large room to review the pages. We would handle any easy changes in that meeting and make sure concerns were handled. Then we would finish up getting the sections ready for state approval and send them on their way. The next week the cycle would start over until we got through all the products.</p>
    </div>

