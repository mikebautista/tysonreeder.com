    <div class="content-container">
      <p>Now that we had a brand guideline we needed to get homepage mockups ready for presentation to the C-level leadership. We worked with the agency to come up with three concepts to present.</p>
      <p>Ultimately we landed on these. Two concepts that were a blend of our design ideas (first two)and one that just my team put together (far right):</p>
    </div>

