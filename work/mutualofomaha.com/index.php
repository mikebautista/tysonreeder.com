    <div class="content-container">
      <p>This is where things got hairy. We were told the scope of the project was existing content with a new visual presentation and some updated navigation and IA.</p>
      <p>It was becoming apparent that this might be a problem so we raised the concern and kept moving on.</p>
      <p>Up until this point there hadn’t been much talk of what the new look and feel for the brand was going to be. It had been communicated to us that the commercial would drive the new branding. I quickly realized that we could not wait for the commercial and that we needed to figure out the feel of the brand as soon as possible to be able to move forward. So we got together with the agency and after a little back and forth we landed on a decision right around Christmas.</p>
    </div>

    <div class="image-container half-size">
